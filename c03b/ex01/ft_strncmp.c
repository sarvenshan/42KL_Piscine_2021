#include <unistd.h>

int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int x;

	x = 0;
	while ((x < n) && (s1[x] != '\0' && s2 != '\0') && (s1[x] == s2[x]))
		x++;
	return (s1[x] - s2[x]);
}
