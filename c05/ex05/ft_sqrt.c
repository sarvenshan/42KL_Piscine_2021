#include <unistd.h>

int	ft_sqrt(int nb)
{
	int x;

	x = 0;
	while ((x * x) < nb)
	{
		x++;
		if (x >= 46341)
			return (0);
	}
	if ((x * x) == nb)
	{
		return (x);
	}
	return (0);
}
