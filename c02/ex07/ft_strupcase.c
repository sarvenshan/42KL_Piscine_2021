#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int x;

	x = 0;
	while (str[x] != 0)
	{
		if (str[x] > 96 && str[x] < 123)
			str[x] -= 32;
		x++;
	}
	return (str);
}
